val = x - y;
console.log(`${x} - ${y} is ${val}`);

val = x * y;
console.log(`${x} * ${y} is ${val}`);

val = x / y;
console.log(`${x} / ${y} is ${val}`);

y = 3;
val = x % y;
console.log(`${x} % ${y} is ${val}`);

let z = 3
val = x * y - z
